import {
  Body1,
  Body1Strong,
  Button,
  Caption1,
  Link,
  makeStyles,
  tokens,
} from '@fluentui/react-components';
import {
  DismissRegular,
  ErrorCircleRegular,
  Document20Regular,
} from '@fluentui/react-icons';
import { useCallback } from 'react';
import useFileUpload, { formatBytes } from '@/hooks/useFileUpload';

export default function Component() {
  const styles = useStyles();
  const maxFiles = 2;

  const {
    files,
    errors,
    handleDrop,
    handleDragEnter,
    handleDragLeave,
    handleDragOver,
    getInputProps,
    removeFile,
    openFileDialog,
    clearFiles,
  } = useFileUpload({ maxFiles, multiple: true });

  const buttonClicked = useCallback(
    (event: React.MouseEvent<HTMLElement>) => {
      event.preventDefault();
      event.stopPropagation();
      openFileDialog();
    },
    [openFileDialog],
  );

  const removeAllButtonClicked = useCallback(() => {
    clearFiles();
  }, [clearFiles]);

  // const isDisabled = files.length >= maxFiles;

  return (
    <div className={styles.root}>
      <label
        className={styles.container}
        onDrag={handleDragOver}
        onDragEnter={handleDragEnter}
        onDragLeave={handleDragLeave}
        onDragOver={handleDragOver}
        onDrop={handleDrop}>
        <input
          {...getInputProps()}
          aria-label='Upload file'
          name='file-upload'
          className={styles.input}
        />

        <FileUploadBackground className={styles.fileUploadBackground} />

        <Body1Strong>
          Upload by clicking here or drag and drop your files
        </Body1Strong>
        <Body1 className={styles.fileUploadDescription}>
          Fill out the <Link>template files</Link>. Kindly fill in the necessary
          data, and once completed, kindly re-upload the files.
        </Body1>

        <Button appearance='primary' onClick={buttonClicked}>
          Upload files
        </Button>
      </label>

      {errors.length > 0
        ? errors.map((error) => (
            <div key={error} className={styles.errorContainer}>
              <ErrorCircleRegular />
              <Body1>{error}</Body1>
            </div>
          ))
        : null}

      {files && files.length ? (
        <>
          <div className={styles.fileListContainer}>
            {files.map((file) => (
              <div key={file.id} className={styles.fileCard}>
                <div className={styles.fileCardNameContainer}>
                  <div className={styles.fileCardIconContainer}>
                    <Document20Regular />
                  </div>
                  <div className={styles.fileNameContainer}>
                    <Body1>{file.file.name}</Body1>
                    <Caption1>{formatBytes(file.file.size)}</Caption1>
                  </div>
                </div>

                <Button
                  size='small'
                  appearance='transparent'
                  shape='square'
                  onClick={() => {
                    removeFile(file.id);
                  }}
                  className={styles.closeButton}>
                  <DismissRegular />
                </Button>
              </div>
            ))}
          </div>
          <Button
            appearance='outline'
            onClick={removeAllButtonClicked}
            className={styles.removeAllButton}>
            Remove all files
          </Button>
        </>
      ) : null}

      <Caption1 className={styles.caption}>
        File uploader w/ fancy background
      </Caption1>
    </div>
  );
}

